// Pair presence_log online→away/offline transitions into work sessions.
const sessionize = (rows) => {
  let openAt = null; const sessions = []; let totalMins = 0;
  for (const r of rows || []) {
    if (r.status === 'online') { if (!openAt) openAt = new Date(r.at); }
    else if (openAt) {
      const end = new Date(r.at); const mins = (end - openAt) / 60000;
      if (mins > 0 && mins < 16 * 60) { sessions.push({ start: openAt.toISOString(), end: end.toISOString(), mins: Math.round(mins) }); totalMins += mins; }
      openAt = null;
    }
  }
  return { sessions, totalMins: Math.round(totalMins) };
};

async function worklog(req) {
  // Detail for one employee (by email): work sessions + per-day hours + leaves.
  if (req.query.email) {
    const email = String(req.query.email);
    const days = Math.min(parseInt(req.query.days, 10) || 30, 90);
    const rows = await queryDb(`select status, at from presence_log where email=$1 and at > now() - ($2 || ' days')::interval order by at asc`, [email, String(days)]).catch(() => []);
    const { sessions, totalMins } = sessionize(rows);
    const byDay = {};
    sessions.forEach((s) => { const d = s.start.slice(0, 10); byDay[d] = (byDay[d] || 0) + s.mins; });
    const empLeaves = await queryDb(`select * from business_leaves where employee_email=$1 order by start_date desc nulls last limit 100`, [email]).catch(() => []);
    return { email, totalHours: +(totalMins / 60).toFixed(1), sessions: sessions.slice(-60).reverse(), byDay, leaves: empLeaves };
  }
  // Summary across all employees (last 7 days).
  const emps = await queryDb(`select id, name, email, title, department, status from business_employees order by name asc`).catch(() => []);
  const emails = emps.map((e) => e.email).filter(Boolean);
  let presence = [];
  if (emails.length) presence = await queryDb(`select email, status, at from presence_log where email = any($1) and at > now() - interval '7 days' order by email asc, at asc`, [emails]).catch(() => []);
  const byEmail = {};
  presence.forEach((r) => { (byEmail[r.email] = byEmail[r.email] || []).push(r); });
  const lc = Object.fromEntries((await queryDb(`select employee_email, sum(days)::float d, count(*)::int n from business_leaves group by employee_email`).catch(() => [])).map((r) => [r.employee_email, { days: r.d, n: r.n }]));
  const employeesOut = emps.map((e) => ({
    ...e,
    hoursWeek: +(sessionize(byEmail[e.email] || []).totalMins / 60).toFixed(1),
    leaveDays: lc[e.email]?.days || 0, leaveCount: lc[e.email]?.n || 0,
  }));
  return { employees: employeesOut };
}

// ── Executive command-center metrics, health score & forecast ─────────────────
const monthKey = (d) => `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`;

async function computeMetrics() {
  const [contactRows, dealRows, campRows, invRows, expRows, empRows] = await Promise.all([
    queryDb(`select type, stage, value, health from business_contacts`),
    queryDb(`select stage, value, probability, status, close_date, updated_at from business_deals`),
    queryDb(`select channel, status, budget, spend, leads, conversions, revenue from business_campaigns`),
    queryDb(`select amount, tax, status, due_date from business_invoices`).catch(() => []),
    queryDb(`select category, amount, status from business_expenses`).catch(() => []),
    queryDb(`select department, status, employment_type, salary from business_employees`).catch(() => []),
  ]);

  const customers = contactRows.filter((c) => c.type === 'customer');
  const leads = contactRows.filter((c) => c.type === 'lead' || c.type === 'prospect');
  const atRisk = contactRows.filter((c) => c.stage === 'at_risk' || (c.type === 'customer' && num(c.health) < 50));
  const churned = contactRows.filter((c) => c.stage === 'churned');

  const openDeals = dealRows.filter((d) => d.status === 'open');
  const wonDeals = dealRows.filter((d) => d.status === 'won');
  const lostDeals = dealRows.filter((d) => d.status === 'lost');
  const pipelineValue = openDeals.reduce((s, d) => s + num(d.value), 0);
  const weightedPipeline = openDeals.reduce((s, d) => s + num(d.value) * (num(d.probability) / 100), 0);
  const wonValue = wonDeals.reduce((s, d) => s + num(d.value), 0);
  const winRate = wonDeals.length + lostDeals.length
    ? Math.round((wonDeals.length / (wonDeals.length + lostDeals.length)) * 100) : 0;

  // Revenue trend — last 6 months of won deals by close/updated month.
  const now = new Date();
  const months = [];
  for (let i = 5; i >= 0; i--) months.push(monthKey(new Date(now.getFullYear(), now.getMonth() - i, 1)));
  const revByMonth = Object.fromEntries(months.map((m) => [m, 0]));
  wonDeals.forEach((d) => {
    const dt = d.close_date ? new Date(d.close_date) : new Date(d.updated_at);
    const k = monthKey(dt);
    if (k in revByMonth) revByMonth[k] += num(d.value);
  });
  const revenueTrend = months.map((m) => ({ month: m, revenue: Math.round(revByMonth[m]) }));

  // Simple linear forecast for next 3 months from the trend slope.
  const ys = revenueTrend.map((r) => r.revenue);
  const n = ys.length;
  const xMean = (n - 1) / 2;
  const yMean = ys.reduce((a, b) => a + b, 0) / (n || 1);
  let numr = 0, den = 0;
  ys.forEach((y, x) => { numr += (x - xMean) * (y - yMean); den += (x - xMean) ** 2; });
  const slope = den ? numr / den : 0;
  const intercept = yMean - slope * xMean;
  const forecast = [];
  for (let i = 1; i <= 3; i++) {
    const dt = new Date(now.getFullYear(), now.getMonth() + i, 1);
    forecast.push({ month: monthKey(dt), revenue: Math.max(0, Math.round(intercept + slope * (n - 1 + i))) });
  }

  // Pipeline funnel by stage.
  const funnel = DEAL_OPEN_STAGES.map((st) => ({
    stage: st,
    count: openDeals.filter((d) => d.stage === st).length,
    value: Math.round(openDeals.filter((d) => d.stage === st).reduce((s, d) => s + num(d.value), 0)),
  }));

  // Marketing roll-up.
  const totSpend = campRows.reduce((s, c) => s + num(c.spend), 0);
  const totRevenue = campRows.reduce((s, c) => s + num(c.revenue), 0);
  const totLeads = campRows.reduce((s, c) => s + num(c.leads), 0);
  const totConv = campRows.reduce((s, c) => s + num(c.conversions), 0);
  const roas = totSpend ? +(totRevenue / totSpend).toFixed(2) : 0;
  const cac = totConv ? Math.round(totSpend / totConv) : 0;
  const byChannel = CAMPAIGN_CHANNELS.map((ch) => {
    const cs = campRows.filter((c) => c.channel === ch);
    return cs.length ? { channel: ch, spend: Math.round(cs.reduce((s, c) => s + num(c.spend), 0)),
      revenue: Math.round(cs.reduce((s, c) => s + num(c.revenue), 0)),
      leads: cs.reduce((s, c) => s + num(c.leads), 0) } : null;
  }).filter(Boolean);

  const conversionRate = totLeads ? Math.round((totConv / totLeads) * 100) : 0;
  const avgCustomerValue = customers.length ? Math.round(customers.reduce((s, c) => s + num(c.value), 0) / customers.length) : 0;
  const ltv = avgCustomerValue * 3; // simple 3x annual-value heuristic
  // No customers → retention is undefined, report 0 (not a misleading 100%).
  const retention = customers.length ? Math.round(((customers.length - churned.length) / (customers.length + churned.length || 1)) * 100) : 0;

  // Composite business health score (0-100) across growth, pipeline, retention, efficiency.
  const pipelineHealth = Math.min(100, Math.round((weightedPipeline / Math.max(wonValue || 1, 1)) * 50 + winRate / 2));
  const churnRisk = customers.length ? Math.round((atRisk.length / customers.length) * 100) : 0;
  const retentionHealth = retention;
  const efficiency = Math.min(100, Math.round(roas * 20 + conversionRate));
  // An empty workspace has no signal — show 0, not a phantom score.
  const hasData = contactRows.length > 0 || dealRows.length > 0 || campRows.length > 0;
  const healthScore = !hasData ? 0 : Math.max(0, Math.min(100, Math.round(
    pipelineHealth * 0.3 + retentionHealth * 0.3 + efficiency * 0.2 + winRate * 0.2
  )));

  // Accounts (finance) roll-up.
  const today = new Date();
  const invTotal = (i) => num(i.amount) + num(i.tax);
  const collected = invRows.filter((i) => i.status === 'paid').reduce((s, i) => s + invTotal(i), 0);
  const outstanding = invRows.filter((i) => i.status === 'sent' || i.status === 'overdue').reduce((s, i) => s + invTotal(i), 0);
  const overdueAr = invRows.filter((i) => i.status === 'overdue' || (i.status === 'sent' && i.due_date && new Date(i.due_date) < today))
    .reduce((s, i) => s + invTotal(i), 0);
  const expensesPaid = expRows.filter((e) => e.status === 'paid' || e.status === 'approved').reduce((s, e) => s + num(e.amount), 0);
  const expensePending = expRows.filter((e) => e.status === 'pending').reduce((s, e) => s + num(e.amount), 0);
  const netCashflow = collected - expensesPaid;
  const expenseByCategory = EXPENSE_CATEGORIES.map((cat) => {
    const total = expRows.filter((e) => e.category === cat).reduce((s, e) => s + num(e.amount), 0);
    return total ? { category: cat, amount: Math.round(total) } : null;
  }).filter(Boolean);

  // HR roll-up.
  const activeEmps = empRows.filter((e) => e.status === 'active' || e.status === 'probation' || e.status === 'on_leave');
  const monthlyPayroll = activeEmps.reduce((s, e) => s + num(e.salary), 0) / 12;
  const headcountByDept = DEPARTMENTS.map((d) => {
    const n2 = empRows.filter((e) => e.department === d && e.status !== 'terminated').length;
    return n2 ? { department: d, count: n2 } : null;
  }).filter(Boolean);

  return {
    headline: {
      revenue: Math.round(wonValue),
      pipelineValue: Math.round(pipelineValue),
      weightedPipeline: Math.round(weightedPipeline),
      openDeals: openDeals.length,
      winRate, customers: customers.length, leads: leads.length,
      atRisk: atRisk.length, conversionRate, ltv, cac, roas, retention,
      avgCustomerValue, healthScore,
    },
    revenueTrend, forecast, funnel, byChannel,
    finance: {
      collected: Math.round(collected), outstanding: Math.round(outstanding), overdueAr: Math.round(overdueAr),
      expensesPaid: Math.round(expensesPaid), expensePending: Math.round(expensePending),
      netCashflow: Math.round(netCashflow), expenseByCategory,
    },
    hr: {
      headcount: empRows.filter((e) => e.status !== 'terminated').length,
      active: activeEmps.length, onLeave: empRows.filter((e) => e.status === 'on_leave').length,
      monthlyPayroll: Math.round(monthlyPayroll), headcountByDept,
    },
    counts: {
      contacts: contactRows.length, deals: dealRows.length, campaigns: campRows.length,
      won: wonDeals.length, lost: lostDeals.length,
      invoices: invRows.length, expenses: expRows.length, employees: empRows.length,
    },
    signals: {
      pipelineHealth, retentionHealth, efficiency, churnRisk,
    },
  };
}


// ── Router ────────────────────────────────────────────────────────────────────
const httpErr = (status, message) => Object.assign(new Error(message), { status });

export default async function handler(req, res) {
  const me = actorOf(req);
  if (!me) return res.status(401).json({ error: 'Not authenticated' });

  // Path segment after /api/business (e.g. metrics, ai, seed) — else resource via ?resource=.
  const seg = (req.path || req.url.split('?')[0]).replace(/^\/api\/business\/?/, '').split('/')[0];
  const resource = seg || (req.query.resource || '');

  try {
    await ensure();
    let out;
    switch (resource) {
      case '': case 'contacts': out = await contacts(req.method, req, me); break;
      case 'deals': out = await deals(req.method, req, me); break;
      case 'campaigns': out = await campaigns(req.method, req); break;
      case 'activities': out = await activities(req.method, req, me); break;
      case 'tasks': out = await tasks(req.method, req, me); break;
      case 'invoices': out = await invoices(req.method, req); break;
      case 'expenses': out = await expenses(req.method, req); break;
      case 'employees': out = await employees(req.method, req); break;
      case 'leaves': out = await leaves(req.method, req); break;
      case 'worklog': out = await worklog(req); break;
      case 'metrics': out = await computeMetrics(); break;
      case 'ai': out = await aiCopilot(req); break;
      case 'seed': out = await seed(me); break;
      default: return res.status(404).json({ error: `Unknown resource: ${resource}` });
    }
    // Activity log — every Growth create/update/delete lands in the admin audit log.
    if (['POST', 'PATCH', 'DELETE'].includes(req.method) && !['ai', 'seed', 'metrics', 'worklog'].includes(resource)) {
      logAudit('growth', me.email, `growth_${resource || 'contacts'}_${req.method.toLowerCase()}`, String((req.body || {}).id || req.query.id || (req.body || {}).name || (req.body || {}).title || '').slice(0, 120)).catch(() => {});
    }
    return res.status(200).json(out);
  } catch (e) {
    if (isMissingTableError(e.message)) return res.status(200).json({ contacts: [], deals: [], campaigns: [], activities: [], tasks: [] });
    return res.status(e.status || 500).json({ error: e.message });
  }
}
